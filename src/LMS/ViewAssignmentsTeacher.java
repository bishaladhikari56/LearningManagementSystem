    public void AccessAllQuestions()
    {
        DefaultTableModel dtm =(DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select assignment.question, assignment.option1,assignment.option2,assignment.option3,assignment.option4\n" +
"from assignment where teacherID=\"" + teacherID + "\" and assignmentName=\""+assignmentName+"\" and courseID=\"" + courseID +"\"");
            while(rs.next())
            {
                dtm.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }

